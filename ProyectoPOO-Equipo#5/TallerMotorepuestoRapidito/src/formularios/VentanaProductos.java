    private void mostrarEnTF(int fila) {
        if (!tproducto.getLista().isEmpty()) {
            tfCodigo.setText(String.valueOf(tproducto.getLista().get(fila).getCodProducto()));
            tfNombre.setText(tproducto.getLista().get(fila).getNombre());
            tfDescripcion.setText(tproducto.getLista().get(fila).getDescripcion());
            tfEstante.setText(tproducto.getLista().get(fila).getEstante());
            tfPrecio.setText(String.valueOf(tproducto.getLista().get(fila).getPrecio()));
            tfStock.setText(String.valueOf(tproducto.getLista().get(fila).getStock()));
            
            esNuevo = false;
            
            int ultReg = tproducto.getLista().size();
            IContador.setText("" + (fila + 1) + " de " + ultReg);
            tfNombre.requestFocus();
            System.out.println("check");
        } else {
            limpiar();
        }
        
    }
