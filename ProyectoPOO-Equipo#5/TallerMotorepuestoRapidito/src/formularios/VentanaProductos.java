    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (tfCodigo.getText().equals("") || tfNombre.getText().equals("") || tfDescripcion.getText().equals("")
            || tfEstante.getText().equals("") || tfPrecio.getText().equals("") || tfStock.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Por Favor Rellene todos los Campos.", "Aviso Taller 'Rapidito' ", 1);

        } else {
            int b=0;
        if (esNuevo){
            b = tproducto.addProducto(Integer.parseInt(tfCodigo.getText()), tfNombre.getText(), tfDescripcion.getText(), tfEstante.getText(),
          Float.parseFloat(tfPrecio.getText()), Integer.parseInt(tfStock.getText()));
        }
        if(b == 1){
            JOptionPane.showMessageDialog(this, "Registro guardado...", "Repuesto",
                    JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            JOptionPane.showMessageDialog(this, "Error inesperado...", "Repuesto",
                    JOptionPane.ERROR_MESSAGE);
        }
        this.mostrarEnTF(0);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnModificar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificar1ActionPerformed

          int b = tproducto.modificarProducto(Integer.parseInt(tfCodigo.getText()), 
          tfNombre.getText(), tfDescripcion.getText(), tfEstante.getText(),
          Float.parseFloat(tfPrecio.getText()), Integer.parseInt(tfStock.getText()));
        if(b == 1){
            JOptionPane.showMessageDialog(this, "Registro modificado correctamente...",
                    "Taller", JOptionPane.INFORMATION_MESSAGE);
            
        }
        else{
            JOptionPane.showMessageDialog(this, "Error inesperado", "Taller", 
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnModificar1ActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        int resp = JOptionPane.showConfirmDialog(this,
                "¿Realmente desea eliminar el registro " + this.tfCodigo.getText()
                + " " + this.tfNombre.getText() + "?", "Taller",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (resp == 0) {
            int b = tproducto.eliminarProducto(Integer.parseInt(tfCodigo.getText()));
            if (b == 1) {
                JOptionPane.showMessageDialog(this, "Registro eliminado satisfactoriamente",
                        "Aviso Taller 'Rapidito'.", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Error inesperado, verifique y vuelva intentar",
                        "Aviso Taller 'Rapidito'", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

