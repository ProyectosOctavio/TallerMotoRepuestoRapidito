    private void mostrarEnTF(int fila) {
        if (!tentrada.getLista().isEmpty()) {
            tfCodigo.setText(String.valueOf(tentrada.getLista().get(fila).getCodProducto()));
            tfNombre.setText(tentrada.getLista().get(fila).getNombre());
            tfDescripcion.setText(tentrada.getLista().get(fila).getDescripcion());
            tfStock.setText(String.valueOf(tentrada.getLista().get(fila).getStock()));
            
            esNuevo = false;
            
            int ultReg = tentrada.getLista().size();
            IContador.setText("" + (fila + 1) + " de " + ultReg);
            tfNombre.requestFocus();
            System.out.println("check");
        } else {
            limpiar();
        }
    }

